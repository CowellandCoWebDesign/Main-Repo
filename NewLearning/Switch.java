public class Switch {
	public static void main(String[] args)
	{
		int NUM_PELMINI = 3;
	
		switch (NUM_PELMINI) 
		{
			case 1 : System.out.println(" you ate 1! ");
			break;

			case 2 : System.out.println(" you ate 2! ");
			break;

			case 3 : System.out.println(" you ate 3! ");
			break;
			
			case 4 : System.out.println(" you ate 4! ");
			break;
		
			case 5 : System.out.println(" you ate 5! ");
			break;
			
			default : System.out.println(" you did not eat any...not hungry?" );
		}
	}
}
